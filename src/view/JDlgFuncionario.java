     public FuncionarioMsl viewBean() {
        FuncionarioMsl funcionario = new FuncionarioMsl();
        
        funcionario.setIdfuncionarioMsl(UtilMsl.strInt(jTxtCodigoMsl.getText()));
        funcionario.setNomeMsl(jTxtNomeMsl.getText());
        funcionario.setEmailMsl(jTxtEmailMsl.getText());
        
        funcionario.setNumeroTelefoneMsl(jFmtCelularMsl.getText());
        funcionario.setCpfMsl(jFmtCpfMsl.getText());
        funcionario.setDepartamentoMsl(jTxtDepartamentoMsl.getText());
        funcionario.setInformacoesMsl(jTxtInformacoesMsl.getText());
        
        
      //  funcionario.setSalarioMsl(null);//jTxtSalarioMsl.getText();

      

        

        return funcionario;
    }
    
    
