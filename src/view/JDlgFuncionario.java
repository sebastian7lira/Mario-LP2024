    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
            
        
         if (UtilMsl.perguntar("Deseja excluir o registro") == true) {

            FuncionarioMsl funcionario = viewBean();
            FuncionarioDAO funcionarioDAO = new FuncionarioDAO();//executa o dao
            funcionarioDAO.delete(funcionario);

        } else {

            UtilMsl.mensagem("Exclusão cancelada.");
        }
        
        
        
        UtilMsl.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,jTxtCodigoMsl,jFmtCpfMsl,jTxtNomeMsl,jFmtCelularMsl,jTxtInformacoesMsl,jTxtEmailMsl,jTxtSalarioMsl,jTxtDepartamentoMsl);
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed

               
          FuncionarioMsl funcionario = viewBean();
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();//executa o dao
        //boolean pra incluir ou alterar
        if(incl == true){
        funcionarioDAO.insert(funcionario);
        
        }else{
        funcionarioDAO.update(funcionario);
        }



        UtilMsl.habilitar(false, jTxtCodigoMsl,jFmtCpfMsl,jTxtNomeMsl,jFmtCelularMsl,jTxtInformacoesMsl,jTxtEmailMsl,jTxtSalarioMsl,jTxtDepartamentoMsl);
        UtilMsl.habilitar(true, jBtnIncluir,jBtnPesquisar);
        UtilMsl.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,jTxtCodigoMsl,jFmtCpfMsl,jTxtNomeMsl,jFmtCelularMsl,jTxtInformacoesMsl,jTxtEmailMsl,jTxtSalarioMsl,jTxtDepartamentoMsl);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

