    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
            
        
         if (UtilMsl.perguntar("Deseja excluir o registro") == true) {

            ProdutoMsl produto = viewBean();
            ProdutoDAO produtoDAO = new ProdutoDAO();//executa o dao
            produtoDAO.delete(produto);

        } else {

            UtilMsl.mensagem("ExclusÃ£o cancelada.");
        }
        
        
        
        UtilMsl.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,jTxtCodigoMsl,jTxtNomeMsl,jTxtDescricaoMsl,jTxtFornecedorMsl,jTxtCategoriaMsl,jTxtPrecoMsl);
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed

               
          ProdutoMsl produto = viewBean();
        ProdutoDAO produtoDAO = new ProdutoDAO();//executa o dao
        //boolean pra incluir ou alterar
        if(incl == true){
        produtoDAO.insert(produto);
        
        }else{
        produtoDAO.update(produto);
        }



        UtilMsl.habilitar(false, jTxtCodigoMsl,jTxtNomeMsl,jTxtDescricaoMsl,jTxtFornecedorMsl,jTxtCategoriaMsl,jTxtPrecoMsl);
        UtilMsl.habilitar(true, jBtnIncluir,jBtnPesquisar);
        UtilMsl.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,jTxtCodigoMsl,jTxtNomeMsl,jTxtDescricaoMsl,jTxtFornecedorMsl,jTxtCategoriaMsl,jTxtPrecoMsl);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

