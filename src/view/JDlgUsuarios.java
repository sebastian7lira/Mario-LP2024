     public UsuariosMsl viewBean() {
        UsuariosMsl usuarios = new UsuariosMsl();
        
        
        usuarios.setIdusuariosMsl(UtilMsl.strInt(JTxtCodigoMsl.getText()));
        usuarios.setNomeMsl(JTxtNomeMsl.getText());
        
        usuarios.setDataNascMsl(null);//jFmtDataNascimento.getText();
        usuarios.setSexoMsl(jJtxtSexoMsl.getText());
        
        usuarios.setEmailMsl(JTxtEmailMsl.getText());
        usuarios.setSenhaMsl(jPwfSenhaMsl.getText());
        

        return usuarios;
    }
    
    
        public UsuariosMsl beanView(UsuariosMsl usuarios){
            
        JTxtCodigoMsl.setText(UtilMsl.intStr(usuarios.getIdusuariosMsl()));
        
        JTxtNomeMsl.setText(usuarios.getNomeMsl());
        
        
        jFmtDataMsl.setText(null);//cliente.getDataNascimento());
        
        jJtxtSexoMsl.setText(usuarios.getSexoMsl());
        JTxtEmailMsl.setText(usuarios.getEmailMsl());
        jFmtCpfMsl.setText(usuarios.getCpfMsl());
       
        jPwfSenhaMsl.setText(usuarios.getSenhaMsl());
        
      
        jBtnAlterar.setEnabled(true);
        return usuarios;
        }        


