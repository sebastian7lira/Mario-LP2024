    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
            
        
         if (UtilMsl.perguntar("Deseja excluir o registro") == true) {

            UsuariosMsl usuarios = viewBean();
            UsuariosDAO usuariosDAO = new UsuariosDAO();//executa o dao
            usuariosDAO.delete(usuarios);

        } else {

            UtilMsl.mensagem("Exclusão cancelada.");
        }
        
        
        
        UtilMsl.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,JTxtCodigoMsl,jFmtCpfMsl,JTxtNomeMsl,jFmtDataMsl,JTxtEmailMsl,jJtxtSexoMsl,jPwfSenhaMsl);
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed

               
          UsuariosMsl usuarios = viewBean();
        UsuariosDAO usuariosDAO = new UsuariosDAO();//executa o dao
        //boolean pra incluir ou alterar
        if(incl == true){
        usuariosDAO.insert(usuarios);
        
        }else{
        usuariosDAO.update(usuarios);
        }



        UtilMsl.habilitar(false, JTxtCodigoMsl,jFmtCpfMsl,JTxtNomeMsl,jFmtDataMsl,JTxtEmailMsl,jJtxtSexoMsl,jPwfSenhaMsl);
        UtilMsl.habilitar(true, jBtnIncluir,jBtnPesquisar);
        UtilMsl.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,JTxtCodigoMsl,jFmtCpfMsl,JTxtNomeMsl,jFmtDataMsl,JTxtEmailMsl,jJtxtSexoMsl,jPwfSenhaMsl);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

